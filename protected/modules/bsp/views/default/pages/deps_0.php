<h3>Компании</h3>
<p>В системе ведется реестр компаний. От компаний выписываются счета, на их имя поступают платежи.</p>
<p>Администратор имеет доступ к списку компаний. 
    Интерфейс позоляет ему производить все необходимые операции: </p>

<ul>
    <li>Заводить новые компании</li>
    <li>Определять какие пользователи будут иметь доступ к компаниям.</li>
    <li>Удалять компании из системы</li>
</ul>

