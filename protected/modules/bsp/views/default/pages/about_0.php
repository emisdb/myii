<h3>Описание</h3>
 <p>Представляемая система является интернет-приложением, предназначенным для ведения финансового учета.</p>
 <p>В системе ведется учет поступающих счетов и их оплат.
     Она позволяет отслеживать состояние оплат счетов, а также систематизировать первичную документацию. 
     </p>
  <p>
     Благодаря реализации в интернете, система дает возможность обеспечить совместную уделенную работу 
     различных пользователей.
 </p>