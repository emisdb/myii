<h3>Перспективы</h3>
 <p>Функционал системы позволяет осуществлять операции любой торгово-закупочной деятельности. 
     Если система не в полной мере соответствует практике, принятой на предприятии заказчика,
     то она может служить основой для изменения системы.
 </p>
 <p>Использование готового шаблона для разработки своей уникальной 
 системы может значительно сократить стоимость проекта. </p>
